/*  =============== Operator ===============*/
// 1. Operator Aritmatika
// +, -, *, /, %
console.log("Operator Aritmatika");
console.log(10 + 10);
console.log(10 - 10);
console.log(10 * 10);
console.log(10 / 10); 
console.log(10 % 10);
console.log(" ");
// 2. Operator Logika
// ||, &&, !;
console.log("Operator Logika")
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false &&false);

console.log(!true);
console.log(!false);
console.log(" ");
// 3. Operator Pembanding
// ==, !=, >, >=, <, <=
console.log("Operator Pembanding");
console.log(1 == '1');
console.log( 1 != '1');
console.log(1 > 1);
console.log(1 >= 1);
console.log(1 < 1);
console.log(1 <= 1);
console.log(" ");
// 4. Operator Identitas
// ===, !==
console.log("Operator Identitas");
console.log(1 === '1');
console.log( 1 !== '1');